/** 获取某 trait 在指定 rank 的完整数值数组 */
export function getTraitRanks(traitId: string, rank: number = 1): number[] {
  const ranks = TRAIT_RANKS[traitId];
  if (!ranks) return [];
  const key = String(rank) as RankKey;
  return ranks[key] ?? ranks['1'] ?? [];
}

/** 获取某 trait 在指定 rank 的某个具体 vars 索引 */
export function getTraitVar(traitId: string, varIdx: number = 0, rank: number = 1): number {
  const vars = getTraitRanks(traitId, rank);
  return vars[varIdx] ?? 0;
}

/** 将 rank 表注入到 TRAITS 对象（在 trait-data.ts 加载后调用） */
export function attachAllRanks(traits: Record<string, { ranks?: Record<number, number[]> }>): void {
  for (const [traitId, ranks] of Object.entries(TRAIT_RANKS)) {
    if (traits[traitId]) {
      const numericRanks: Record<number, number[]> = {};
      for (const [key, val] of Object.entries(ranks)) {
        numericRanks[Number(key)] = val;
      }
      traits[traitId].ranks = numericRanks;
    }
  }
}
